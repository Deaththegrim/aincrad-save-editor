/// The active language's strings.
pub fn s(lang: Lang) -> &'static S {
    match lang {
        Lang::En => &EN,
        Lang::Ja => &JA,
        Lang::Fr => &FR,
        Lang::De => &DE,
        Lang::Es => &ES,
        Lang::PtBr => &PT,
        Lang::It => &IT,
        Lang::Ru => &RU,
        Lang::ZhHans => &ZH,
        Lang::Ko => &KO,
    }
}

